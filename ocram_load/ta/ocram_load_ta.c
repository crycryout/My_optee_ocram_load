 /* TA命令分发入口 */
 
 TEE_Result TA_InvokeCommandEntryPoint(void *session,
    uint32_t command_id,
    uint32_t param_types,
    TEE_Param params[4])
{
TEE_Result res = TEE_ERROR_BAD_PARAMETERS;
uint32_t err_orig = 0;
(void)session;

switch (command_id) {
    case TA_OCRAM_LOAD_CMD_INC_VALUE:
        DMSG("INC_VALUE 被调用");
        res = TEE_SUCCESS;
        break;
    
    case TA_OCRAM_LOAD_CMD_DEC_VALUE:
        DMSG("DEC_VALUE 被调用");
        res = TEE_SUCCESS;
        break;
    
    case TA_OCRAM_LOAD_CMD_MAP_MEMORY:
        DMSG("MAP_MEMORY 未实现");
        res = TEE_ERROR_NOT_IMPLEMENTED;
        break;
    
    case TA_OCRAM_LOAD_CMD_STORE: {
        const uint32_t exp_pt =
            TEE_PARAM_TYPES(TEE_PARAM_TYPE_MEMREF_INPUT,
                            TEE_PARAM_TYPE_NONE,
                            TEE_PARAM_TYPE_NONE,
                            TEE_PARAM_TYPE_NONE);
        if (param_types != exp_pt)
            return TEE_ERROR_BAD_PARAMETERS;
        /* 存储到 Secure Storage */
        TEE_ObjectHandle obj;
        res = TEE_CreatePersistentObject(TEE_STORAGE_PRIVATE,
                                         MODEL_DATA_OBJ_ID,
                                         strlen(MODEL_DATA_OBJ_ID),
                                         TEE_DATA_FLAG_ACCESS_READ |
                                         TEE_DATA_FLAG_ACCESS_WRITE |
                                         TEE_DATA_FLAG_ACCESS_WRITE_META |
                                         TEE_DATA_FLAG_OVERWRITE,
                                         TEE_HANDLE_NULL,
                                         NULL, 0,
                                         &obj);
        if (res != TEE_SUCCESS) return res;
        res = TEE_WriteObjectData(obj,
                                  params[0].memref.buffer,
                                  params[0].memref.size);
        if (res != TEE_SUCCESS) {
            TEE_CloseAndDeletePersistentObject1(obj);
            return res;
        }
        TEE_CloseObject(obj);
        DMSG("STORE: 存储 %zu 字节成功", params[0].memref.size);
        res = TEE_SUCCESS;
        break;
    }
    
    case TA_OCRAM_LOAD_CMD_LOAD: {
        /* 调用 load-PTA */
        TEE_TASessionHandle s1;
        res = TEE_OpenTASession(&pta_ocram_load_uuid, 0,
                                TEE_PARAM_TYPES(TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE),
                                NULL, &s1, &err_orig);
        if (res != TEE_SUCCESS) return res;
        TEE_Param pt[4] = {};
        pt[0].memref.buffer = input_data;
        pt[0].memref.size   = sizeof(input_data);
        res = TEE_InvokeTACommand(s1,
                                  TEE_TIMEOUT_INFINITE,
                                  OCRAM_LOAD_CMD,
                                  TEE_PARAM_TYPES(TEE_PARAM_TYPE_MEMREF_INPUT,
                                                  TEE_PARAM_TYPE_NONE,
                                                  TEE_PARAM_TYPE_NONE,
                                                  TEE_PARAM_TYPE_NONE),
                                  pt, &err_orig);
        TEE_CloseTASession(s1);
        if (res == TEE_SUCCESS)
            DMSG("LOAD: 已通过 PTA 加载 %zu 字节", sizeof(input_data));
        else
            EMSG("LOAD: PTA 加载失败, 0x%x", res);
        break;
    }
    
    case TA_OCRAM_LOAD_CMD_READ: {
        /* 调用 read-PTA */
        const uint32_t exp_pt =
            TEE_PARAM_TYPES(TEE_PARAM_TYPE_MEMREF_OUTPUT,
                            TEE_PARAM_TYPE_NONE,
                            TEE_PARAM_TYPE_NONE,
                            TEE_PARAM_TYPE_NONE);
        if (param_types != exp_pt)
            return TEE_ERROR_BAD_PARAMETERS;
        TEE_TASessionHandle s2;
        res = TEE_OpenTASession(&pta_ocram_read_uuid, 0,
                                TEE_PARAM_TYPES(TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE,
                                                TEE_PARAM_TYPE_NONE),
                                NULL, &s2, &err_orig);
        if (res != TEE_SUCCESS) return res;
        TEE_Param pt[4] = {};
        /* 外部传入一个 buffer 和 size */
        pt[0].memref.buffer = params[0].memref.buffer;
        pt[0].memref.size   = params[0].memref.size;
        res = TEE_InvokeTACommand(s2,
                                  TEE_TIMEOUT_INFINITE,
                                  OCRAM_READ_CMD,
                                  exp_pt, pt, &err_orig);
        if (res == TEE_SUCCESS) {
            /* PTA 返回实际读回的长度 */
            params[0].memref.size = pt[0].memref.size;
            DMSG("READ: 已读回 %u 字节", (unsigned)pt[0].memref.size);
        } else {
            EMSG("READ: PTA 调用失败, 0x%x", res);
        }
        TEE_CloseTASession(s2);
        break;
    }
    
    default:
        EMSG("不支持的命令 ID: %u", command_id);
        res = TEE_ERROR_BAD_PARAMETERS;
        break;
    }
    
    return res;


return res;
}
